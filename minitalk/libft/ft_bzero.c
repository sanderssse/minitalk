#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*s1;
	int		i;

	s1 = (char *)s;
	i = 0;
	while (n > 0)
	{
		s1[i] = 0;
		i++;
		n--;
	}
}
